//Variables

var a = 10;
var b = 20;



//Addition

function addSum(param1, param2) {
    console.log(param1 + param2);
}
addSum(a,b);



//Subtraction

function subDiff(param1, param2) {
    console.log(param1-param2);
}
subDiff(a,b)



//Multiplication

function proSum(param1, param2) {
    console.log(param1*param2);
}
proSum(a,b)



//Division

function quoDiff(param1, param2) {
    console.log(param1/param2)
}
quoDiff(a,b)